<h2>The Solution</h2>
<p id="solutionParagraph">SkillCourt is developing a concept that provides to the Soccer public a game that captures statistical

metrics, always incorporates a ball for any activities, has any level of physical difficulty but at the same

time is always geared to improve the user’s cognitive skills. The game is considered for the individual

although two or more players could play versions of games fitting to the particular number of users. The

patent pending combination of the software & hardware sets up capabilities of endless variations

starting off with one pad only. The goal is to help all soccer players, clubs, organizations, families, and

businesses with a tool that brings the users closer to their true physical & mental potential playing the

game of Soccer. The pads can be laid out, mounted, or installed in places where space and the proper

surface allows. The focus is to have a user friendly app as a personal guide for the user and technology

where the illumipad’s aesthetic charms is presented through its lightness, durability and overall outlook

design.</p>
