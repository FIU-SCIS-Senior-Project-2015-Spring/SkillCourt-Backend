<h2>Introduction</h2>
<p id="introductionParagraph" class="aboutUsParagraph">SkillCourt presents its patent pending game, an interactive system consisting of a software application

and illuminated target pads called illumipad. The shockproof surface on each individual illumipad has

the ability to completely light up to represent a target for users to hit with a Soccer ball. When a ball hits

the surface of particular target the illumipads interior touch & pressure sensors immediately reports

back to app for live feedback. Via app, the illumipads communicate wirelessly through Bluetooth or Wi-

Fi to initiate different training exercises where user performance is documented and statistics reported.

The pads provide a multiple color illuminations to guide the user for a particular game, the more pads

the more targets user can play with.</p>
