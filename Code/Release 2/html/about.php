<h2>The Challenge</h2>
<p id="challengeParagraph" class="aboutUsParagraph">Soccer players at all ages lack the exposure to efficient commercialized Soccer technology. Every player

who wants to improve would for example love to receive any measurable statistics about their

individual performance. Also, there hasn’t been a lot of breakthrough technological advancements in

products for Soccer players to use when practicing individually outside of practice hours. Combine that

with the ongoing neuroscientific research by leading experts in sports which emphasizes the importance

on the athlete’s brain power (i.e. cognitive skills). Neuroscience suggests that brain powering activities

plays a huge role when players prepare physically & mentally for those key moments during games to

execute a move at the right time with the desired motor skill. All Soccer enthusiasts know that with each

move on the field could have a domino effect on good things or bad to come.</p>
